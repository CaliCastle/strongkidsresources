		<blockquote class="margin-v">
			[Mindfulness] involves a willingness to drop in on yourself, to live more in the present moment, to stop at
			times and simply be rather than get caught up in endless doing while forgetting who is doing all the doing
			and why. It has to do with not "mis-taking" our thoughts for the truth of things, and not being so
			susceptible to getting caught in emotional storms, storms that so often only compound pain and suffering,
			our own and that of others…you don’t have to do anything other than to pay attention and stay awake and
			aware. (Kabat-Zinn, 2013, pg. xxxi, Full Catastrophe Living)
		</blockquote>
		<p class="margin-v">
			As we reviewed the literature to incorporate relevant scientific and practical updates to the 2nd edition,
			we found that mindfulness-based practices are being used with youth and in schools more frequently and with
			increasing enthusiasm. The following literature caught our attention:
		</p>
		<ul class="circle margin-v">
			<li>Mindfulness practices, while rooted in the Buddhist tradition, have been secularized and applied to
				mental health and wellness and researched over the past 30 years
			</li>
			<li>
				The Mindfulness-Based Stress Reduction Program (Kabat-Zinn) has been a pioneer example of how this
				application can be helpful for:
				<ul>
					<li>pain and stress (e.g., Rosenzweig et al., 2011;Marchand, 2012),</li>
					<li>anxiety and depression (e.g., Ramel, et al., 2004; Segal et al., 2002), and</li>
					<li>increasing positive emotional states (e.g., Brown &amp; Ryan, 2003).</li>
				</ul>
			</li>
			<li>Aspects of mindfulness can be complementary to a cognitive-behavioral approach to social, emotional, and
				behavioral awareness and regulation (e.g., Broderick &amp; Metz, 2009)
			</li>
			<li>Brief practices can be used in schools (e.g., Flook, Goldberg, Pinger, Bonus, &amp; Davidson, 2013)</li>
			<li>Preliminary studies evaluating the use of mindfulness with youth in school settings are finding positive
				findings, with more research needed (see Burke, 2010; Harnett &amp; Dawe, 2012; Meiklejohn et al., 2012;
				and Zenner et al., 2014, for reviews of this literature).
			</li>
		</ul>
		<p class="margin-v">
			Following an extensive study of the application of mindfulness practices with youth and in schools, we
			decided aspects of mindfulness have the potential to compliment the curriculum. Strong Kids is not a
			mindfulness-centered curriculum. We have preserved the wellness promotion and cognitive-behavioral
			orientation of the program, while infusing practices and language the reflect aspects of a mindfulness-based
			approach to awareness and regulation. By infusing brief practices into the Strong Kids programs, we aim to
			increase students’ awareness of their emotions, thoughts, and behaviors, their ability to manage and
			regulate emotions and behaviors, take others’ perspectives, develop healthy and reciprocal relationships,
			make responsible decisions.
		</p>
		<p class="margin-v">
			Each lesson begins with a mindfulness-based practice called a "Mindfulness-Based Focusing Activity." In
			keeping with the first edition of Strong Kids, scripts are provided for ease of use. We anticipate that some
			instructors will not have experience with mindfulness-based practices and will appreciate the guidance
			provided from these scripts, as well as supplementary information and resources provided in Chapter 4 of the
			program manuals. All activities are designed to be optional to accommodate those who may wish to opt out.
			Language is infused throughout the lessons to reframe otherwise overtly judgmental language, such as "good"
			and "bad," with more constructively reflective words such as "helpful" and "unhelpful." Feedback to date
			suggests these activities to be well-received.
		</p>
		<p class="margin-v">
			For additional information regarding our reasoning and process used to incorporate mindfulness into the
			Strong Kids programs, see Gueldner and Feuerborn, 2015.
		</p>
