		<h4 class="section-heading">
			Strong Teens: Facilitating Student Engagement
		</h4>

		<br>

		<p>
			Hello! I implemented Strong Teens at an alternative high school as a Tier 1 intervention. The student body
			was split into two groups, with each group receiving the curriculum for 12-weeks. My journey included both
			the organizational components of demonstrating a need to the staff and district office, securing resources
			(time, funding, individuals to lead the group, materials, etc.) as well as the “boots on the ground”
			components of leading one of the student groups, preparing weekly materials and adapting “on the fly” to
			facilitating student participation/buy-in. Its been an exciting journey from start to finish! Here are a few
			thoughts for others who are interested in trying out the curriculum:
		</p>

		<ul class="circle">
			<li><b>Identify and select facilitators with care and think about contextual issues:</b> Student buy-in was
				key for me. The minute I started saying “I’ve got this great program, we’re going to talk about life
				skills such as conflict management, managing stress….” My students’ gave me an earful of negative
				reasons about why they didn’t need to participate and/or had already learned this “stuff” in elementary
				school. I quickly learned that I needed to be strategic in the language I used to frame the program, as
				well as the fact that I needed build social capital into my program. By social capital, I mean this:
				adult role models that the students related to and were comfortable around. For our programming, we ran
				2 groups with 2 adults each. Each group included a known male and female staff member. Let’s be honest,
				teenage boys aren’t thrilled or readily willing to talk about their feelings in front of a mixed group
				of their peers. So, the addition of a male leader was key to that connectivity, especially for my school
				population. Moreover, I also needed to reframe the groups as “Classes” vs. “Groups.” In my school,
				groups were for people with “problems,” as in: “you have no social skills so you have to meet with the
				school psychologist.” Also, we held the groups in classrooms, with students at desks and used the
				whiteboard (PowerPoint) to facilitate a classroom vs small group feel.
			</li>
			<li><b>Think carefully about student grouping.</b> The co-facilitators and I used a tw0-pronged approach to
				the groups. First, we randomly split the student body in half and then split it into halves again. The
				initial group (e.g., half of the student population) was random with a few exceptions. Importantly, we
				considered the relationships between students and whether those relationships could help or hinder the
				group dynamic. We also sought to include 1-2 students with high social status in all groups. Despite
				that pre-work, we still had challenges with student dynamics throughout the course of the classes. For
				example, one student’s presence or absence, and corresponding mood could easily deter half of the
				classes’ engagement and derail active engagement. We had students rally small groups, (e.g., 2-3) and
				they would play upon one other, snickering, deterring the conversation, etc. We had individuals try to
				switch groups, demanding that they were unfairly placed into a class – even bargaining that they would
				participate more if they were allowed to switch groups. The students definitely kept us on their toes!
				We dealt with this by lots of debriefs before and after classes, being consistent with students about
				expectations, and working hard to engage them while in groups. We didn’t fight every battle. We didn’t
				respond to every side comment – a little sharing and self-directed humor can go a long way to get
				students to engage. On many occasions, we were able to link in these conflicts to a Strong Teens
				lessons, e.g., identifying triggers to negative emotions and physical reactions, using a problem-solving
				model, etc. Interestingly, some of our loudest negative voices were the ones who showed up on time and
				provided the best examples.
			</li>
			<li>
				<b>Lesson Preparation: Get comfortable with the curriculum and be prepared that you might not cover
					everything. That’s ok!</b> For our groups, we created PowerPoints to help facilitate a classroom
				feeling but also to ensure we were consistently delivering the curriculum. I put everything from direct
				text straight from the manual, images, the corresponding supplemental handouts, videos and photos into
				the PowerPoint. Before each week, the facilitators would meet to review the lesson, talk about what
				components would be highlighted, agree on presentation style, etc. I found this presentation format was
				helpful to keep us on track during presentations, but moreover, it was necessary in order to include the
				technology upgrades to better engage my students. For example, by Lesson 2 I quickly noticed that I was
				losing students who had mental blocks on talking about emotions, let alone listening to me talk about
				emotions! So, I went looking for supplements. For example, I grabbed images off the internet about the
				body’s physiological reactions to experiencing emotions and I inserted videos (brief videos, e.g., 2-3
				minutes tops) that explained physiology, e.g., flight vs. flight. These seemed to help. For Lesson 3 I
				had an epiphany: students relate better to things in their culture, popular figures, movies, etc.! I
				inserted images of Taylor Swift and Kayne West (VMA 2009) and Matt Lauer and Tom Cruise (talking about
				Scientology) to introduce understanding and reading non-verbal language. That sparked some really great
				conversations and took us deeper than the examples listed in the curriculum. Moving on from that, I
				started scanning UTube and the internet to input images and videos for every lesson. I included
				educational things (e.g., short videos more scientifically based) as well as humorous (e.g., clips from
				movies, UTube shorts, funny images with animals, cartoons etc.). Some particularly successful moments
				were:
				<ul>
					<li>Lesson 5: I used videos to give vision to the definitions (e.g., Anchorman clip *** edited to
						define aggression) and Anger Management clip to define anger management). I also used clips from
						Elf, Mulan, UTube (shorts) to exemplify different aspects of anger and anger management
						strategies.
					</li>
					<li>Lesson 6 and 7 (Thinking Traps): Clips from Seinfeld, Charlie Brown (he evidences so many
						different thinking traps), Star Wars (there is a great compilation on UTube that shows Luke
						Skywalker demonstrating almost every Thinking Trap in the curriculum), and School of Rock. I
						also found some great images to demonstrate perception taking skills (e.g., skewed images) and
						comics on Google images.
					</li>
				</ul>
			</li>
			<li>
				<b>Know your students.</b> I knew many but not all of the students in my groups prior to implementation
				of the curriculum. One important learning was to find each student’s hook. Talking about emotions and
				coping strategies is a lot to ask of any teenager, let alone asking them to do it in front of their
				peers. A few useful tidbits that I feel might help:
				<ul>
					<li><b>Pull in students with social status to be leaders.</b> I had them lead the meditation
						practice, a lesson component that was a particularly hard sell to my students throughout the
						entire class. Having them decide how to read it, include music, lights off, etc. seemed to help.
						Essentially, let them own it a little.
					</li>
					<li><b>Notice what they care about.</b> My students were quick to engage with examples that included
						social media. They seemed turned off to anything related to grades or sports. Therefore, I used
						as many examples that tapped into social media that I could find. I solicited their feedback. I
						even used an image of a IPhone volume slider versus a thermometer to demonstrate “emotional
						intensity” (early lessons).
					</li>
					<li><b>Identify a way for everyone to comfortably participate.</b> This means giving jobs to those
						who might be less comfortable with verbally sharing. For example, for us, this meant reading the
						examples, working the PowerPoint, etc.
					</li>
					<li><b>A little sharing and humor can go a long way.</b> In order for students to feel safe to
						share, they need to have trust in the environment and not feel threatened. Makes sense. On more
						than one occasion, I shared, admitted miseries from my past, and put myself out there as an
						example for them to problem-solve. It worked. Sometimes it was only a laugh, e.g., “please,
						please stop dancing… we understand the emotions are you feeling but we are feeling the
						opposite,” to get them back on track and engaged. Despite protests that they would “never” share
						their emotions, we had students share some pretty specific, intense feelings and experiences. I
						think everyone got out of it what they needed and participated at a level that felt comfortable
						– and that was my goal.
					</li>
				</ul>
			</li>
		</ul>
		<p></p>

		<p>
			Going forward, my plan is to use Strong Teens as a curriculum for entering 9th graders as part of a Freshman
			Academy. I continue to add lots of ideas to my arsenal for individual lessons. Keeping current on popular
			culture, listening to what students are talking about, etc.… its all good material!
		</p>

