		<p>
			A notable benefit of the Strong Teens curriculum is its versatility. Users can take advantage of the
			scripted lessons or use their own words. They can also supplement and adapt program examples to match the
			needs of their students. Based on feedback from users and practitioners, the following represents a sampling
			of activities and tips that have been found to positively benefit student engagement and application of
			programming materials.
		</p>

		<hr>
		<b>Student Selection &amp; Grouping</b>
		<hr>
		<p>When forming student groups, consider the unique personalities, cognitive capacities, social skills, and
			social dynamics between proposed members.</p>
		<ul>
			<li>It may be important to include individuals with strong social standing to help facilitate student
				buy-in, e.g., those who are comfortable sharing ideas, speaking in front of the group, etc.
			</li>
			<li>It may be helpful to limit individuals with (a) significant personal conflicts or (b) who are close
				personal friends (e.g., BFFs) to limit the group from getting off course and/or shifting power dynamics
				between participants.
			</li>
			<li>The size of the group can highly influence the range and level of participation that individual students
				will be able to engage in. Note, the larger the group, the more "psycho-educational" versus
				personal/reflective it will be.
			</li>
		</ul>

		<br>

		<p>The time and place for your group is probably dictated by several non-negotiables.</p>
		<ul>
			<li>Strive for a consistent day/time to meet so that students can easily anticipate and come prepared to
				participate. Any changes in the meeting time should be provided in advance.
			</li>
			<li>Users have suggested that the time of day (end) and day of the week (Fridays) can be particularly
				difficult to engage students in group activities due to shifts in focus and fatigue.
			</li>
		</ul>
		<p></p>

		<br>

		<p>
		</p>
		<hr>
		<b>Program Presentation</b>
		<hr>
		<p>Curriculum materials can be presented in a variety of ways.</p>
		<ul>
			<li>Use Technology. All program supplements are available as downloads. Also, timely and relevant video
				clips, images, and music are great ways to engage students in the program content.
			</li>
			<li>Lessons include worksheets and homework. Have paper handouts prepared.</li>
			<li>Homework. Depending upon the unique dynamics of your students, consider having homework to be completed
				within the group meeting minutes. For example, having students complete homework at the end of the
				corresponding lesson or at the start of the proceeding lesson. Either way, the focus is on extension of
				learning through guided thought/practice.
			</li>
			<li>
				Splitting lessons. The curriculum materials offer guidelines for "Running Short On Time" and suggestions
				for segmenting lessons. When splitting lessons, best practice would be to keep the lessons as close
				together as possible to facilitate applicability to prior teaching. Some specific tips:
				<ul>
					<li>Loop back to teachings and discussion that previously occurred within the lesson. Facilitators
						can either prompt student memory via specific components or have them re-review a previously
						completed activity.
					</li>
					<li>Have students "report out" what they remember from the prior lesson. This could even be a
						specific "job" that each group member has, rotated across lessons so that all individuals have a
						chance to participate.
					</li>
				</ul>
			</li>
		</ul>
		<p></p>

