		<b>Supplemental Components</b>
		<hr>
		<p>For all age ranges, extension materials can offer opportunities to benefit engagement and generalization.
			Some user friendly resources include:</p>
		<ul>
			<li>Photos/Images: These can include anything from celebrities, popular movie/TV characters, animals
				demonstrating human qualities, etc. These are great for facilitating recognition of emotional states,
				levels of intensity, reading non-verbal behavior, and predicting antecedents and consequences of
				behavior.
			</li>
			<li>Music: Music is a great vehicle for talking about emotional states, physical reactions, and levels of
				intensity. In addition, it is a natural extension of the mindfulness-based focusing activity and
				identification of coping strategies, e.g., calming strategies.
			</li>
			<li>
				Videos: There are an abundance of video (e.g., movie clips, TV clips, UTube shorts, structured
				presentations) options for you to consider. Practitioners have found them particularly useful as
				follows:
				<ul>
					<li>Describing the cognitive processes (physiological) to experiencing emotions, including "fight
						vs. flight"
					</li>
					<li>Identifying emotional states, verbal and non-verbal behavior, levels of intensity, and
						problem-solving steps.
					</li>
					<li>Identifying "Thinking Traps"</li>
				</ul>
			</li>
		</ul>
