		</p>
		<hr>
		<b>Curriculum Delivery</b>
		<hr>
		<p>Depending on the maturity and skills of your students, curriculum delivery (e.g., engagement, facilitation,
			etc.) will be vastly different.</p>
		<ul>
			<li>
				Strong Start
				<ul>
					<li>
						The younger the student, the higher level of structure is required. This means clearly defined
						expectations that are consistently delivered. Furthermore, the ratio of physical
						activity/practical applications to verbal (lecture) delivery is pivotal.
						<ul>
							<li>Include hands on activities whenever possible. Students will benefit from a multi-modal
								experience: seeing, hearing, doing, etc.
							</li>
							<li>Consider technology: videos, music. Whenever possible, loop in "high-value" characters
								from popular media/culture to facilitate relevance and buy-in.
							</li>
							<li>Consider older age students to be "guest" facilitators or helpers. These individuals
								will have high social value to the younger students and are great for engaging students
								in role plays, practice, etc. Moreover, these students can be great for informal
								"check-ins" with students as they see them throughout the school day, thus promoting
								further generalization.
							</li>
							<li>Consider having parents be "guest" to the group. They can offer additional examples and
								scenarios to build generalization beyond the school day.
							</li>
						</ul>
					</li>
				</ul>
			</li>
			<li>
				Strong Kids
				<ul>
					<li>
						Upper elementary and middle school students are adjusting to new responsibilities and have
						unique developmental considerations. Curriculum delivery will need to provide a balance between
						facilitator control and opportunities for individualization.
						<ul>
							<li>Consider including a structured extrinsic reinforcement system to support student
								participation and meaningful engagement.
							</li>
							<li>Carefully consider those individuals who struggle with social interaction (e.g., comfort
								with sharing thoughts/ideas). Creating special job roles, such as homework collector or
								question reader, may help them feel like valued members of the group and ease their
								comfort with participation. For example, (a) homework collector, (b) question reader,
								etc.
							</li>
							<li>For small group work, provide a structured "selection process" from the beginning, e.g.,
								counting off by 1, 2, etc. Students benefit from consistent but fair expectations and
								rules and are less likely to resist if you are consistent with your methodology.
							</li>
						</ul>
					</li>
				</ul>
			</li>
			<li>
				Strong Teens
				<ul>
					<li>
						High school students typically can handle higher level roles and responsibilities in group
						settings. However, when it comes to talking about emotions and feelings, they can also be highly
						resistant. Opportunities to build upon desires for control, allow for personal voice/expression,
						and applicability to real life scenarios are key for success. Consider:
						<ul>
							<li>For older students, structuring the group as a "psycho-educational" class versus "group"
								may be necessary. This verbiage may be more palatable to students who are resistant to
								participating.
							</li>
							<li>Have students select special roles or jobs such as reading the mindfulness-based
								focusing activity and reading examples and scenarios.
							</li>
							<li>Link examples and scenarios to issues relevant to students whenever possible. This
								includes asking for their feedback and extending current curriculum materials, as well
								as generating student specific examples.
							</li>
							<li>
								Today’s youth spend a considerable amount of time accessing media, including listening
								to music, watching TV, texting, and checking social media. Tap into this preferred
								medium by using technology to present lesson material as well as make materials more
								relatable to students.
								<ul>
									<li>Use examples referencing all social media vehicles (e.g., Snapchat, Instagram,
										etc).
									</li>
									<li>Consider music for lessons and mindfulness-based focusing activities as derived
										from student driven selections (e.g., smart phones)
									</li>
									<li>Share common APPS related for personal extension: meditation, organization,
										etc.
									</li>
								</ul>
							</li>
							<li>
								To facilitate comfort with the mindfulness-based focusing activity, consider:
								<ul>
									<li>Having students read the activity components</li>
									<li>Think about environment: body position, light, music, etc.</li>
								</ul>
							</li>
							<li>Consider including popular figures and events from popular media. For example, when
								presenting "Thinking Traps," consider incorporating clips from popular television and
								movies.
							</li>
							<li>Teenagers can sometimes be resistant to the appearance of needing or accepting
								assistance with social-emotional health. Facilitators should consider incorporating
								personal stories and humor to aid buy-in and comfort.
							</li>
						</ul>
					</li>
				</ul>
			</li>
		</ul>
