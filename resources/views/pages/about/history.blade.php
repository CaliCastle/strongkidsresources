		<p class="left margin-v">
			The <em>Strong Start, Strong Kids, and Strong Teens</em> programs were developed under the direction of the
			late
			Kenneth W. Merrell, PhD and with the Oregon Resiliency Project (ORP) at the University of Oregon (see
			Merrell, 2010 for more information). At the time, the literature revealed alarming trends and significant
			challenges:
		</p>
		<div class="center margin-v"><img src="/images/sel_matters.jpg" alt=""></div>
		<ul class="circle margin-v">
			<li>12%-22% of students under the age of 18 years were experiencing mental health problems so severe they
				needed services (Greenberg et al., 2001)
			</li>
			<li>75-85% of the students in need of mental health services were not receiving adequate treatment (U.S.
				Department of Health and Human Services, 1999)
			</li>
			<li>Schools are often the primary and essential provider for mental health services due to high demand and
				lack of coordinated resources and without an increase in person power to deliver these services
				(Hoagwood &amp; Erwin, 1997)
			</li>
			<li>Increasing discussion of how mental health problems can lead to academic problems, school drop out, and
				stress existing resources (Greenberg et al., 2003).
			</li>
		</ul>
	</div>
	<div class="container">
		<p class="left margin-v">
			Meanwhile, an interest in applying a public health model to schools to improve students’ well-being was
			strongly emerging. The evidence for school-based prevention programs focused on “social and emotional
			learning” (SEL) was growing, demonstrating that schools have a role in promoting health and reducing risk
			(Greenberg et al, 2003). As with any development and expansion of new technology, glitches remained that
			needed attention: staff needed initial and ongoing support, programs needed ways to be sustained over time,
			schools had to thoughtfully consider how programs would be infused into the overall school culture and
			coordinate with other efforts, and progress monitoring was imperative to measure outcomes. As a result,
			efforts to make positive change were sometimes fragmented and inefficient (see Greenberg et al., 2003 for
			this discussion).
		</p>
	</div>
	<div class="container margin-v">
		<p class="left margin-v">
			<b>Strong Kids was developed to address the following areas:</b>
		</p>
		<ul class="circle margin-v">
			<li>Prevention and resiliency-building focus</li>
			<li>Evidence-based theory and methods, with a focus on cognitive-behavioral theory</li>
			<li>Instructionally sound teaching and learning methods</li>
			<li>Practical content and materials</li>
			<li>Cost-effective</li>
			<li>Easy to use</li>
			<li>Content taught during the course of a typical class period</li>
			<li>Accessible to students from pre-Kindergarten to Grade 12</li>
			<li>Logical alignment with the goals of schools</li>
			<li>Developmentally appropriate, relevant and engaging content</li>
			<li>
				Focus on teaching and learning:
				<ul>
					<li>Skills known to prevent internalizing problems (e.g., depression, anxiety, somatic complaints,
						social withdrawal)
					</li>
					<li>Identifying and increasing awareness regarding emotions</li>
					<li>Cognitive restructuring</li>
					<li>Empathy and perspective taking</li>
					<li>Helpful communication skills</li>
					<li>Interpersonal problem solving skills</li>
					<li>Stress reduction</li>
					<li>Anger management</li>
					<li>Behavioral change and goal setting</li>
				</ul>
			</li>
			<li>Assessment of fidelity of implementation and outcomes</li>
			<li>Support available for staff development and program implementation</li>
		</ul>
		<p class="left margin-v">
			<b>Strong Kids users are encouraged to consider the following upon embarking upon implementation:</b>
		</p>
		<ul class="checkmark">
			<li>How will the program/s fit into the school’s goals and current curricula?</li>
			<li>How will the goals of the program/s help your students meet their goals?</li>
			<li>What types of resources are needed for support?</li>
			<li>How will the school involve all students, faculty, and staff?</li>
			<li>How will the school involve families?</li>
			<li>How will implementation and progress be measured?</li>
		</ul>
	</div>
	<div class="container margin-v">
		<p class="left margin-v">
			You will find many of these tips and associated materials available on this website!
		</p>
		<p class="left margin-v">
			Since we first began work on the Strong Kids series, evidence continues to support that SEL prevention and
			intervention efforts are associated with students’ academic, behavioral, social, and emotional success
			(Durlak, et al., 2011; Sklad, et al., 2012). Skills such as critical thinking, problem solving,
			communication, and making responsible decisions are now believed to be essential to students’ academic
			success (Partnership for 21st Century Skills, 2008; Pellegrino & Hilton, 2013). We are very pleased that the
			Strong Kids curriculum series continues to contribute to the overall development of our 21st Century youth.
		</p>
	</div>
