		<b class="left margin-v">We have added several elements to all the lessons which include: (a) SEL competencies
			(described previously), (b) suggestions for when instructors are running short on time, (c) instructor
			reflection, (d) optional focusing activity, (e) extension activities, and (f) fidelity checklists. These
			components are described below:</b>
		<ul class="circle margin-v">
			<li>Running Short on Time? We recognize that teachers often do not have a segment of time long enough to
				complete an entire lesson. In response to this observation, we have included this new section at the
				start of each lesson. This section provides suggestions for appropriate places to consider stopping the
				lesson—–-places where it would be relatively easy for teachers to pick up and finish at another time.
			</li>
			<li>Instructor Reflection. This segment provides teachers with guiding questions that will help them reflect
				on lesson content as they experience it individually and as they observe students experiencing it in the
				classroom. Taking this time to reflect may make teaching the concepts easier.
			</li>
			<li>Optional Focusing Activity. Practitioners may choose to introduce mindfulness practices within the
				curriculum. <a href="http://strongkidsresources.com/research/mindfulness" target="_blank">Initial research</a> on secularized approaches suggests that developmentally appropriate,
				regular mindfulness practice can help children pay attention, feel calm, engage in fewer problem
				behaviors, feel less anxious, and so forth. Example practices may include paying attention to one’s
				breathing and/or the sounds in the environment and identifying feelings and where in the body those
				feelings are experienced.
			</li>
			<li>Extension Activities: At the end of each lesson, students are given the opportunity to reflect on the
				lesson by means of journal prompts (k-2 specifically) or creative activities. Entries can be written or
				drawn, depending on the development of the students. The activities provided are different for each
				lesson and include art projects, interactive games, suggested videos, and so forth. We envision teachers
				incorporating such activities into center time or, large- or small- group time.
			</li>
			<li>Fidelity Checklists. We have included fidelity checklists with each lesson. These checklists include
				what we consider to be the critical components of each lesson. We suggest that teachers use these
				checklists to track their implementation, to identify parts that might have been missed, to address
				missing parts at a later time, and so forth. Practitioners may also like to self-monitor the extent to
				which lessons are being implemented as designed within the curriculum. We suggest that teachers use
				these checklists to track their implementation, to identify parts that might have been missed, to
				address missing parts at a later time, and so forth.
			</li>
		</ul>
