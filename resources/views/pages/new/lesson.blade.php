			<b class="margin-v">The 10 Strong Start Lessons</b>
			<br>
			<p>Lesson 1: The Feelings Exercise Group
				Students are introduced to the Strong Start curriculum, and specifically the purpose, goals,
				expectations, and general practices.</p>
			<br>
			<p>Lessons 2 and 3: Understanding Your Feelings
				Students are engaged in activities intended to improve their emotional vocabulary, awareness, and
				resiliency skills. Students learn that, although it is okay to have any feeling, there are appropriate
				and inappropriate ways of showing or expressing feelings.</p><br>
			<p>
				Lesson 4: Understanding Other People’s Feelings
				Students focus on discerning and understanding the feelings that other people experience to positively
				inform their emotional awareness skills.
			</p><br>
			<p>
				Lesson 5: When You’re Angry
				Students develop an understanding of anger, and specifically, recognition of anger, common situations
				that might lead people to feel angry, and how to appropriately respond when others are angry.
			</p><br>
			<p>
				Lesson 6: When You’re Happy
				Students develop an understanding of happiness, and consider the connections among this feeling, their
				thoughts, and various contexts. Students are also introduced to cognitive restructuring techniques to
				reframe and view situations more positively.
			</p><br>
			<p>
				Lesson 7: Learning Appropriate Techniques to Manage Stress
				Students are taught to apply specific behavioral, affective, and cognitive skills and strategies to
				situations that might cause them worry and anxiety.
			</p><br>
			<p>
				Lesson 8 - Being a Good Friend
				Students are introduced to skills that foster effective interpersonal communication. Skills such as
				using a nice voice, being a good listener, making appropriate eye contact, and using appropriate body
				language are emphasized.
			</p><br>
			<p>
				Lesson 9: Solving People Problems
				Students build upon their skill-set by considering ways to effectively resolve conflicts with their
				peers. Learning these skills may serve as a preventive factor for deterring emotional as well as social
				problems.
			</p><br>
			<p>
				Lesson 10: Finishing Up!
				Students review key points and terms from the lessons presented throughout the previous several weeks.
				This lesson emphasizes celebrating the accomplishments that have been made through involvement with the
				Strong Start curriculum
			</p><br>
