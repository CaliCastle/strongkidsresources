				<td>Introduces the concept of physical feelings associated with emotions. Normalizes feelings of discomfort. Includes strategies to promote mindfulness of emotions, thoughts, and physical feelings.</td>
			</tr>
			<tr>
				<td>3. Understanding Your Emotions (2)</td>
				<td>Introduces the concept of emotions, thoughts, and behaviors interacting. Includes discussion of the ways in which behaviors communicate emotions, including helpful and unhelpful behaviors. Includes discussion of avoidance or repression of feelings. Considers the context in the expression of emotions. The way we show how we are feeling depends on where we are, who we are with, what we want/need in the situation.</td>
