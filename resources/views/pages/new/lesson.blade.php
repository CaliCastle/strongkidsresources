				<td>5. Dealing with Anger</td>
				<td>Frames the reaction to anger as a range of choices. Introduces the option of not reacting when experiencing anger. Includes concepts pertaining to complex emotions. Anger can be experienced along with emotions like sadness or jealousy.</td>
			</tr>
			<tr>
				<td>6. Clear Thinking (1)</td>
				<td>"Thinking errors" are now defined as "thinking traps." Introduces two new cognitive distortions or thinking traps: "All alone," the feeling that one’s experiences are so unique that no one else could possibly understand; and "broad brush," overgeneralizing from one experience, often at play in the development of stereotypes. Addresses another cognitive distortion, the belief that one’s thoughts are reality.</td>
			</tr>
			<tr>
				<td>7. Clear Thinking (2)</td>
				<td>Introduces the concept of "snowball effect"; i.e., one thinking trap can lead to more thinking traps or an accumulation of pessimistic thinking. Includes scenarios that address perfectionistic thinking; an example pertains to perfectionistic thoughts about body image. Includes discussion about identifying patterns or habits of thought, in addition to intensity as a way to identify distorted perceptions, Includes more concrete analogies and images to help students understand the concept of reframing.</td>
			</tr>
			<tr>
				<td>8. Solving People Problems</td>
				<td>Emphasizes respectful and responsible choices about personal behavior based on social norms, realistic evaluation of consequences of various actions, and the well-being of self and others. Includes expanded options to resolving conflicts using a problem-solving model. Includes gray areas or less clear-cut examples.</td>
			</tr>
			<tr>
				<td>9. Letting Go of Stress</td>
				<td>Reframes stress, shifting from a perception that the stressor is always harmful to one that stress can pose a healthy challenge. Illustrates how to evaluate expectations as realistic or unrealistic. Includes procrastination and avoidance. Includes more activities to help students cope with stress; e.g., nature, physical activity, deep breathing, diet.</td>
			</tr>
			<tr>
				<td>10. Positive Living</td>
				<td>Replaces the previous edition’s Positive Thinking lesson. Includes ways to promote a sense of personal control and facilitate the development of healthy habits and activities across school, home, and community settings. Provides students with the opportunity to assess their daily routines and whether or not they are helpful. Introduces the concepts of balance and evaluating actions that may have short-term and/or long-term benefits.</td>
			</tr>
			<tr>
				<td>11. Creating Strong &amp; Smart Goals</td>
				<td>Introduces setting SMART (specific, measureable, attainable, relevant, and timely) goals. Promotes self-assessment in areas of physical and emotional health and school, family, and community connections. Identification of personal strengths and areas for growth. Emphasizes effort and a growth mindset to facilitate determination and perseverance after setbacks.</td>
			</tr>
			<tr>
				<td>12. Finishing UP!</td>
				<td>Provides more integration of critical concepts found across all lessons. Promotes awareness of when help may be needed. Includes a section on accessing community resources and where to go for help. Emphasizes resilience and persistence.</td>
